    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //Get Values
        String codee = code.getText();
        String lnamee = lname.getText();
        String typee = type.getText();
        double payratee = Double.parseDouble(payrate.getText());
        String thourss = thours.getText();
        String ohourss = ohours.getText();
        String ahourss = ahours.getText();
        //Ensure that there is a value
        if(thourss.equals("") || ohourss.equals("") || ahourss.equals("")){
            JOptionPane.showMessageDialog(this, "Incomplete Record");
            return;
        }
        //Ensure That Values inputted are Numbers and not Negative
        if(thourss.matches("(\\d*\\.?\\d+)|(\\.\\d+)") && ohourss.matches("(\\d*\\.?\\d+)|(\\.\\d+)") && ahourss.matches("(\\d*\\.?\\d+)|(\\.\\d+)")){
        }
        else{
            JOptionPane.showMessageDialog(this, "Input Contains Alphabetic Characters");
            return;
        }
        if(thourss.contains("-") || ohourss.contains("-") || ahourss.contains("-")){
            JOptionPane.showMessageDialog(this, "Input Contains Negative Value");
            return;
        }
        //Convert to Double now that it is Verified
        double dthours = Double.parseDouble(thourss);
        double dohours = Double.parseDouble(ohourss);
        double dahours = Double.parseDouble(ahourss);
        if(typee.equals("Non-Teaching Employee")){
            //For NT Employees
            if(dohours > 48){
                JOptionPane.showMessageDialog(this, "Overtime Cannot Exceed 48 Hours");
                return;
            }
            //Get Additional Values
            double otrate = (payratee/30/8)*1.20;
            double otpay = dohours*otrate;
            double allowancee = 0;
            double deductionss = dahours * (payratee/30/8);
            //Calculate Values
            //Allowance?
            int choice = JOptionPane.showConfirmDialog(this, "Include Allowance?");
            if(choice == JOptionPane.YES_OPTION){
                boolean correct = false;
                do{
                    String percentage = JOptionPane.showInputDialog("Enter Percentage:");
                    if(percentage != null){
                        try{
                            double percent = Double.parseDouble(percentage);
                            if(percent > 10 || percent <= 0){
                                JOptionPane.showMessageDialog(this, "Invalid Percentage");
                            }
                            else{
                                percent = percent/100;
                                allowancee = payratee*percent;
                                correct = true;
                            }
                        }
                        catch (NumberFormatException e){
                            JOptionPane.showMessageDialog(this, "Enter Valid Percentage");
                        }
                    }
                    else{
                        return;
                    }
                }
                while(correct == false);
            }
            else if(choice == JOptionPane.NO_OPTION){
                allowancee = 0;
            }
            else{
                return;
            }
            double salaryy = (payratee + otpay + allowancee) - deductionss;
            //Save Final Values
            thoursf = 0;
            ohoursf = dohours;
            ahoursf = dahours;
            //Convert to 2 Decimals
            DecimalFormat dformat = new DecimalFormat("#.##");
            //Show Values
            otpayfield.setText(dformat.format(otpay));
            allowance.setText(dformat.format(allowancee));
            deductions.setText(dformat.format(deductionss));
            salary.setText(dformat.format(salaryy));
        }   
        else{
            //For T Employees
            //Can't be lower than 48 Hours of Work
            if(dthours < 48){
                JOptionPane.showMessageDialog(this, "Teaching Hours Must be at least 48 Hours");
                return;
            }
            if(dthours > 96){
                JOptionPane.showMessageDialog(this, "Teaching Hours Cannot Exceed 96 Hours");
                return;
            }
            //Overload can't Exceed 32 Hours
            if(dohours > 32){
                JOptionPane.showMessageDialog(this, "Overload Hours Cannot Exceed 32 Hours");
                return;
            }
            //Absent Hours cannot be more than 48 Hours
            if(dahours < 48){
            }
            else{
                JOptionPane.showMessageDialog(this, "Absent Hours Cannot be 48 Hours or More");
                return;
            }
            //Get Additional Values
            double opay = dohours * .120;
            double allowancee = (dthours * payratee) * .10;
            double deductionss = payratee * dahours;
            //Calculate Values
            double salaryy = (dthours * payratee + opay + allowancee) - deductionss;
            //Save Final Values
            thoursf = dthours;
            ohoursf = dohours;
            ahoursf = dahours;
            //Convert to 2 Decimals
            DecimalFormat dformat = new DecimalFormat("#.##");
            //Show Values
            otpayfield.setText(dformat.format(opay));
            allowance.setText(dformat.format(allowancee));
            deductions.setText(dformat.format(deductionss));
            salary.setText(dformat.format(salaryy));
        }
        calculate = true;
    }//GEN-LAST:event_jButton1ActionPerformed

